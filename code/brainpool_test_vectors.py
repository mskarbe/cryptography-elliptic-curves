from dataclasses import dataclass

from types import KeyPair, SharedKey, Point


@dataclass
class BrainpoolTestVector:
    a: KeyPair
    b: KeyPair
    sh: SharedKey


def getBrainpoolTestVector(
    a_priv: int,
    a_x: int,
    a_y: int,
    b_priv: int,
    b_x: int,
    b_y: int,
    sh_x: int,
    sh_y: int,
) -> BrainpoolTestVector:
    return BrainpoolTestVector(
        a=KeyPair(public=Point(x=a_x, y=a_y), private=a_priv),
        b=KeyPair(public=Point(x=b_x, y=b_y), private=b_priv),
        sh=SharedKey(key=Point(x=sh_x, y=sh_y), sharedWith="test-vector"),
    )


